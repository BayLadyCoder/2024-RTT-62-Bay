package org.example.practice_assignments.pa_303_2_3;

public class Task6 {
    public static void main(String[] args) {
        // Write a program that demonstrates at least three ways
        // to increment a variable by 1 and does this multiple times.
        // Assign a value to an integer variable, print it,
        // increment by 1, print it again, increment by 1, and then print it again.

        int num = 0;
        System.out.println(num);
        num = num + 1;
        System.out.println(num);
        num++;
        System.out.println(num);
        num += 1;
        System.out.println(num);
        ++num;
        System.out.println(num);
    }
}
